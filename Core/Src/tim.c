void HAL_TIM_PWM_PulseFinishedCallback(TIM_HandleTypeDef *htim)
{
  if(htim->Instance == TIM1){
    if(LED_State.top1){
      LED_State.top1--;
      if(LED_State.top1 == 0){
        HAL_TIM_PWM_Stop_DMA(htim,CHANNEL_TOP1);
      }
    }else if(LED_State.top2){
      LED_State.top2--;
      if(LED_State.top2 == 0){
        HAL_TIM_PWM_Stop_DMA(htim,CHANNEL_TOP2);
      }
    }
  }else if(htim->Instance == TIM2){
    if(LED_State.mid1){
      LED_State.mid1--;
      if(LED_State.mid1==0){
        HAL_TIM_PWM_Stop_DMA(htim,CHANNEL_MID1);
      }
    }else if(LED_State.mid2){
      LED_State.mid2--;
      if(LED_State.mid2==0){
        HAL_TIM_PWM_Stop_DMA(htim,CHANNEL_MID2);
      }
    }else if(LED_State.bot1){
      LED_State.bot1--;
      if(LED_State.bot1==0){
        HAL_TIM_PWM_Stop_DMA(htim,CHANNEL_BOT1);
      }
    }
  }else if(htim->Instance == TIM3){
    if(LED_State.bot2){
      LED_State.bot2--;
      if(LED_State.bot2==0){
        HAL_TIM_PWM_Stop_DMA(htim,CHANNEL_BOT2);
      }
    }
  }
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  //use swap chain to draw water arrow
  if(htim->Instance == TIM4){
    if(LED_State.mid1_arrow){
      static uint8_t *temp_data = arrows+1152;
      uint8_t *t = mid1_arrow;
      mid1_arrow = temp_data;
      temp_data = t;
      memcpy(mid1_arrow+192,temp_data,960);
      memcpy(mid1_arrow,temp_data+960,192);
      LED_State.mid1 = 8;
      HAL_TIM_PWM_Start_DMA(&TIM_MID1,CHANNEL_MID1,(uint32_t*)mid1_arrow,1152);
    }else if(LED_State.mid2_arrow){
      static uint8_t *temp_data = arrows+3456;
      uint8_t *t = mid2_arrow;
      mid2_arrow = temp_data;
      temp_data = t;
      memcpy(mid2_arrow+192,temp_data,960);
      memcpy(mid2_arrow,temp_data+960,192);
      LED_State.mid2 = 8;
      HAL_TIM_PWM_Start_DMA(&TIM_MID2,CHANNEL_MID2,(uint32_t*)mid2_arrow,1152);
    }else{
      HAL_TIM_Base_Stop_IT(&htim4);
    }
  }
}
